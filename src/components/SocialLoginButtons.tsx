import { useState } from "react";
import { useAuth } from "@/context/AuthContext";

interface SocialLoginButtonsProps {
  onError?: (error: string) => void;
  className?: string;
}

/**
 * Social login buttons for all supported providers.
 * Includes Firebase (Google, GitHub) and OAuth2 (Discord, LinkedIn, Steam).
 */
export default function SocialLoginButtons({ onError, className = "" }: SocialLoginButtonsProps) {
  const {
    loginWithGoogle,
    loginWithGithub,
    loginWithDiscord,
    loginWithLinkedIn,
    loginWithSteam,
  } = useAuth();

  const [loading, setLoading] = useState<string | null>(null);

  const handleLogin = async (provider: string, loginFn: () => Promise<void>) => {
    setLoading(provider);
    try {
      await loginFn();
    } catch (error) {
      console.error(`${provider} login failed:`, error);
      onError?.(error instanceof Error ? error.message : `${provider} login failed`);
    } finally {
      setLoading(null);
    }
  };

  const buttonBaseClass = `
    w-full flex items-center justify-center gap-3 px-4 py-3 
    rounded-lg font-medium transition-all duration-200
    border border-[#2A2A2A] hover:border-[#3A3A3A]
    disabled:opacity-50 disabled:cursor-not-allowed
  `;

  return (
    <div className={`space-y-3 ${className}`}>
      {/* Firebase Providers */}
      <button
        onClick={() => handleLogin("google", loginWithGoogle)}
        disabled={loading !== null}
        className={`${buttonBaseClass} bg-white text-gray-900 hover:bg-gray-100`}
      >
        {loading === "google" ? (
          <LoadingSpinner />
        ) : (
          <GoogleIcon />
        )}
        Continue with Google
      </button>

      <button
        onClick={() => handleLogin("github", loginWithGithub)}
        disabled={loading !== null}
        className={`${buttonBaseClass} bg-[#24292e] text-white hover:bg-[#2f363d]`}
      >
        {loading === "github" ? (
          <LoadingSpinner />
        ) : (
          <GithubIcon />
        )}
        Continue with GitHub
      </button>

      <div className="relative my-4">
        <div className="absolute inset-0 flex items-center">
          <div className="w-full border-t border-[#2A2A2A]" />
        </div>
        <div className="relative flex justify-center text-sm">
          <span className="px-2 bg-[#0D0D0D] text-gray-500">Or continue with</span>
        </div>
      </div>

      {/* OAuth2 Providers */}
      <button
        onClick={() => handleLogin("discord", loginWithDiscord)}
        disabled={loading !== null}
        className={`${buttonBaseClass} bg-[#5865F2] text-white hover:bg-[#4752C4]`}
      >
        {loading === "discord" ? (
          <LoadingSpinner />
        ) : (
          <DiscordIcon />
        )}
        Continue with Discord
      </button>

      <button
        onClick={() => handleLogin("linkedin", loginWithLinkedIn)}
        disabled={loading !== null}
        className={`${buttonBaseClass} bg-[#0A66C2] text-white hover:bg-[#004182]`}
      >
        {loading === "linkedin" ? (
          <LoadingSpinner />
        ) : (
          <LinkedInIcon />
        )}
        Continue with LinkedIn
      </button>

      <button
        onClick={() => handleLogin("steam", loginWithSteam)}
        disabled={loading !== null}
        className={`${buttonBaseClass} bg-[#1b2838] text-white hover:bg-[#2a475e]`}
      >
        {loading === "steam" ? (
          <LoadingSpinner />
        ) : (
          <SteamIcon />
        )}
        Continue with Steam
      </button>
    </div>
  );
}

